fun main(){
    var a = 0
    a = 1 + 1  // penjumlahan
    a = 1 * 1  // perkalian
    a = 1 / 1  // pembagian
    a = 1 - 1  // pengurangan
    a = 1 % 1  // sisa pembagian

    // Augmented asssignmnt
    a+=1
    a-=1
    a/=1
    a*=1
    a%=1

    // Unary Operator
    a++
    a--
    println(a)
}